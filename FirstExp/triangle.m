axis([0,0.1,0,6]);
xlabel('time');
ylabel('前3项有限级数');
y=3+a*(cos(100*pi*t)+cos(3*100*pi*t)/9+cos(5*100*pi*t)/25+cos(7*100*pi*t)/49+cos(9*100*pi*t)/81);
