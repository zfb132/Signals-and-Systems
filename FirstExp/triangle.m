%周期对称三角波合成
t=0:0.001:0.1;
% a是根据傅里叶分解计算的乘法系数
a=24/(pi*pi);
y=3+a*cos(100*pi*t);
subplot(221);
plot(t,y);
axis([0,0.1,0,6]);
xlabel('time');
ylabel('前1项有限级数');
y=3+a*(cos(100*pi*t)+cos(3*100*pi*t)/9)+cos(5*100*pi*t)/25;
